    <p>Image galleries breath visual life into the content layout of our page. In this class, and previously in WEB110, the main type of gallery module we’ve learned about is the Lightbox, but my research indicates there are many others; Gallery 2, zenphoto, and Minishowcase are just a few of those that top the list suggested by one site. </p>

<p>We build our galleries from our image folder in our project. So the user can scan through a gallery slider themselves, we use jquery sliders, for which there are a number of widgets available online. One site tells us:</p>

<p>“To add a slider widget to your page, use a standard input with the type="range" attribute. The input's value is used to configure the starting position of the handle and the value is populated in the text input. Specify min and max attribute values to set the slider's range. If you want to constrain input to specific increments, add the step attribute. Set the value attribute to define the initial value. The framework will parse these attributes to configure the slider widget.” </p>

        <p>Topping another list, is the Layerslider, which they tell us:</p>

<p>“…uses cutting edge technologies to provide the smoothest experience that’s possible, and it comes with more than 200 preset 2D and 3D slide transitions. 
 It’s device friendly by supporting responsive mode, multiple layouts, touch gestures on mobile devices, and uses techniques like lazy load for optimal performance.”</p>

<p>It’s good to know there is such a plethora of gallery options available, because I have a feeling this feature will be playing a prominent role in my future designs.</p>

