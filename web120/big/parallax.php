        <p>BIG Research Doc #5: Parallax</p>

<p>Here I’d like to discuss my research findings in parallax scrolling, which the W3 defines as, “…a web site trend where the background content (i.e. an image) is moved at a different speed than the foreground content while scrolling.”   Although it’s not always effective on mobile devices, we can use media queries to work around that issue.  Parallax is an innovative type of scroll, “…where images move to give off a parallax effect. As the user scrolls down the page, the animations are triggered off and overall it gives a fresh look and feel to any website if implemented correctly.” </p>

<p>Using plugins is advisable when creating this type of scroll because as one site puts it, 
“Making a parallax scrolling site is often tedious because it requires in-depth knowledge of CSS, Javascript  and good web design to pull off.”  </p>

<p>Parallax is a great way for indulging the contemporary web users limited attention span as well, because as one user testing blog says, “The web user of today wants to get entertained and involved. We are more than willing to engage with a website, if we are invited to do so anyway. With parallax scrolling you can do a bunch of great things to invite your visitors to engage with your site.” </p>

<p>Many users today make the decision whether to engage with, and stick around on a site in an unconscious, instantaneous manner, and parallax scrolls are a great way to make sure their engaged with the page as soon as they start scrolling.</p>

