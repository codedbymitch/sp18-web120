   <p><b>BIG Research Doc #2: Shopping Carts</b></p>

<p>We’ve all interacted with shopping carts when we reach the end of our experience of an online retailer, at which point some of us complete the commercial transaction while a surprising amount abandon the purchase all together. As one site noted: “Shopping cart software are systems developed to serve as order catalogues for order stores. In most cases, shopping carts act as the interface between the store’s front end and back end, making it possible for customers to choose products/services, read the description and reviews of what they’ve chosen, add/edit settings, and make a purchase.” </p>

	<p>There are many different programs you can use to design your shopping cart, with, according to pcmag, Wix being the most popular.  E-commerce says Apple has the most “inspirational” shopping cart.   Other sites weigh the benefits of having a mini or full size cart, and examine the psychological effect of having a cart say “empty” versus reminding the customer that they “still need to add items.” (ecommerceuxdesign) </p>

<p>Researching this document I was intrigued to discover that there is quite a bit of discussion about the future of shopping carts and wish lists- which are a function of shopping carts. Just yesterday I had a frustrating experience with an online retailer that would not let me wishlist items I was browsing until I’d set up an account, which seemed a cumbersome and unnecessary flaw in the site’s design. So I was amused when the first site I went to for this research paper was discussing just this dilemma, known as, I found out, ‘’wishlist gating’’.  (Goinflow)</p>

	<p>The biggest question surrounding the gating issue seems to be: the advantage of getting customer info by requiring them to create an account versus allowing them the freedom and flexibility to come in and note items of interest at whim without a signup process. But this is only one detail in the discussion about shopping carts and their utilization.</p>

	<p>I’ve found a great deal of research into examining the question of why some customers abandon their shopping carts once it comes time to purchase. One site says this is because at each point of shopping on the site the customer has felt in control until they reach the cart, at which point something changes: “…once the consumer begins the shopping cart process, he/she loses all of this control. The consumer must follow and conform to a series of forward steps dictated by the retailer, delivering the necessary information deemed necessary BY the retailer, to satisfy the retailer's needs for a transaction.”  </p>

	<p>This is important because on the SEO side of things, shopping cart monetization has become a popular way for retailers to maximize their presence in search engines so that they can compete with large distributors. Here, retailers pay a fee to the search engine in exchange for listing in commercial search results. As one site describes, this allows “…retailers a better chance to influence shoppers’ purchasing decisions, a move that is likely to help them compete with rival Amazon.com Inc. Google hopes the program helps retailers capture more purchases on desktop, cell phones and smart home devices with voice search — the next frontier for e-commerce.” (venturebeat)  This idea came about after google realized that there were a great deal of searches devoted to finding out where products can be bought, ending with a purchase on amazon, and this would hopefully redirect this traffic back to the retailers own shopping cart.</p>

	<p>This has been my favorite research paper so far, because it got me thinking about my own interactions with e-commerce and the subtle ways that a site’s design unconsciously influences me to go through with purchases… how it can impact my general feeling about how enjoyable a site is, with this entire thought process going on under my surface awareness. I will now be paying keener attention to how my own online shopping experiences can inform my skills as a designer.	</p>

	




