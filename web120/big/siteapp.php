        <p>BIG Research Doc #1 Responsive Website vs Native Mobile App</p>

<p>Because websites will often contain too much information for mobile devices to display, the web designer must consider what will be the best way to get condense the information to a scale that will be manageable on mobile devices. The two most popular options for this are a responsive site, or use of mobile apps. (thinkmobiles) This paper will reflect my research into what the differences are, and what the pros and cons are of each.</p> 
	<p>As I understand it, responsive sites switch to a new design once the viewport shrinks past a certain breakpoint, whereas mobile apps are designed specifically for mobile devices. As Loonar puts it: “Generally, there are several differences between mobile applications and responsive web design. Mobile applications are specific products that become a popular choice for many companies that want to offer their customers a perfect experience. They are customized to each OS that feature unique codebases and designs.” Whereas, “Responsive design, on the other hand, generally means that a website is created specifically for mobile viewing, and it will adapt to whatever size of the screen the visitor wants.” (Loonars) </p>

	<p>Mobile apps are good for companies as they take on a featuring role on the person’s device, becoming a fixture of it. The client and designer are able to craft an experience that is customarily designed for their customers and can also send data back to the company on what’s working and what isn’t. Mobile apps are faster and can offer other features that would be impossible on a responsive site. </p>

<p>Responsive sites use something called css media queries which allow “…websites content to fit into any type of screen both 17’ laptop or 3’ phone.”  (Clearbridge) This allows greater adaptability across a wide range of platforms and devices, but require more work and attention from designers and take longer to load. This is especially useful for newer companies looking for fresh customers, whom, being unfamiliar with the product offered are unlikely to go to an app store and download a new app. I would say from personal experience this is true for old customers as well, because having apps for everything can crowd and clutter your device. Responsive sites are cheaper and faster to design than mobile apps.</p>

<p>It’s my perspective that responsive sites are necessary for any company because the general web is more likely to be the first place customers will look for you, but a mobile app presence is also important to stay competitive in today’s online marketplace, so I’d certainly encourage my client to opt for both.</p>

